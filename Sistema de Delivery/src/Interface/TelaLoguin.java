    private void btnLoguinCActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoguinCActionPerformed
        // TODO add your handling code here:
        ConexaoSQlite conexaoSQlite = new ConexaoSQlite();
        conexaoSQlite.conectar();

        ResultSet resultSet = null;
        PreparedStatement pstmt = null;

        String sql1 = "SELECT *"
                + " FROM tbl_cliente"
                + " WHERE email = ?"
                + " AND senha = ?";

        try {

            pstmt = conexaoSQlite.criarPreparedStatement(sql1);
            pstmt.setString(1, txtLoguin.getText());
            pstmt.setString(2, passSenha.getText());

            resultSet = pstmt.executeQuery();
            
            Cliente clienteLogado = new Cliente();
            clienteLogado.setId(resultSet.getInt(1));

            if (resultSet.next()) {
                
                String name = resultSet.getString(3);
                TelaInicialCliente telainicialC = new TelaInicialCliente(clienteLogado);
                TelaInicialCliente.lblNomeC.setText(name);
                TelaInicialCliente.emailC.setText(txtLoguin.getText());
                telainicialC.setVisible(true);
                dispose(); // fechar tela de login
                
                pstmt.close();
                conexaoSQlite.desconectar();

            } else {
                JOptionPane.showMessageDialog(null, "Usu치rio e/ou Senha inv치lido");
                pstmt.close();
                conexaoSQlite.desconectar();
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }


    }//GEN-LAST:event_btnLoguinCActionPerformed

